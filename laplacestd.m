function SD = laplacestd(b)

% function SD = laplacestd(b)
%
%   example call: laplacestd(1);
%
% standard deviation of laplace distribution from scale parameter (b)
%
% b:     scale parameter
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% SD:    standard deviation
%
%               *** see laplace*.m ***

% LAPLACE STANDARD DEVIATION
SD = sqrt(2).*b;
